/**
 *  Problem 3. Assign Variables
 */

public class Problem3 {

    public static void main(String[] args) {
        byte firstVariable = 127;
        short secondVariable = 32767;
        int thirdVariable = 2000000000;
        long fourthVariable = 919827112351L;
        char fifthVariable = 'c';
        boolean sixthVariable = false;
        float seventhVariable = 0.5f;
        double eightVariable = 0.1234567891011;
        String ninthVariable = "Palo Alto, CA";
        System.out.println(firstVariable);
        System.out.println(secondVariable);
        System.out.println(thirdVariable);
        System.out.println(fourthVariable);
        System.out.println(fifthVariable);
        System.out.println(sixthVariable);
        System.out.println(seventhVariable);
        System.out.println(eightVariable);
        System.out.println(ninthVariable);

    }
}
